CREATE TABLE countries (
  country_code   VARCHAR2(10) PRIMARY KEY,
  country_name   VARCHAR2(120) NOT NULL,
  risk_class     VARCHAR2(40) NOT NULL
);

CREATE TABLE customers (
  customer_id    VARCHAR2(30) PRIMARY KEY,
  full_name      VARCHAR2(120) NOT NULL,
  customer_type  VARCHAR2(30) NOT NULL,
  country_code   VARCHAR2(10) NOT NULL REFERENCES countries(country_code),
  risk_score     NUMBER(5,2) DEFAULT 0
);

CREATE TABLE accounts (
  account_id     VARCHAR2(30) PRIMARY KEY,
  account_type   VARCHAR2(40) NOT NULL,
  open_date      DATE NOT NULL,
  status         VARCHAR2(30) NOT NULL
);

CREATE TABLE account_owners (
  account_id     VARCHAR2(30) NOT NULL REFERENCES accounts(account_id),
  customer_id    VARCHAR2(30) NOT NULL REFERENCES customers(customer_id),
  owner_role     VARCHAR2(40) DEFAULT 'PRIMARY',
  CONSTRAINT account_owners_pk PRIMARY KEY (account_id, customer_id)
);

CREATE TABLE devices (
  device_id           VARCHAR2(30) PRIMARY KEY,
  device_fingerprint  VARCHAR2(120) NOT NULL,
  first_seen_ts       TIMESTAMP
);

CREATE TABLE logins (
  login_id       VARCHAR2(30) PRIMARY KEY,
  account_id     VARCHAR2(30) NOT NULL REFERENCES accounts(account_id),
  device_id      VARCHAR2(30) NOT NULL REFERENCES devices(device_id),
  login_ts       TIMESTAMP NOT NULL
);

CREATE TABLE transactions (
  transaction_id   VARCHAR2(30) PRIMARY KEY,
  from_account_id  VARCHAR2(30) NOT NULL REFERENCES accounts(account_id),
  to_account_id    VARCHAR2(30) NOT NULL REFERENCES accounts(account_id),
  amount           NUMBER(12,2) NOT NULL,
  currency_code    VARCHAR2(3) DEFAULT 'USD',
  channel          VARCHAR2(40),
  transaction_ts   TIMESTAMP NOT NULL
);

CREATE TABLE investigation_cases (
  case_id          VARCHAR2(30) PRIMARY KEY,
  alerted_account  VARCHAR2(30) NOT NULL REFERENCES accounts(account_id),
  alert_type       VARCHAR2(120) NOT NULL,
  case_status      VARCHAR2(30) DEFAULT 'OPEN',
  priority         VARCHAR2(20) DEFAULT 'MEDIUM',
  created_ts       TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE case_notes (
  note_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  case_id       VARCHAR2(30) NOT NULL REFERENCES investigation_cases(case_id),
  note_text     CLOB NOT NULL,
  created_by    VARCHAR2(120) DEFAULT USER,
  created_ts    TIMESTAMP DEFAULT SYSTIMESTAMP
);


